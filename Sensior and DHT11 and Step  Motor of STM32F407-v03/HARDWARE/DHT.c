//采用单总线方式读取数据
//引脚要同时配置为输入输出，必须外接上拉电阻【传感器引脚已经外接】
//STM32程序中要配置为开漏输出  OD
//输入的时候，要先将引脚置高


#include "DHT.h"

u16 wendu=40;
u16 shidu=50;

   
//复位DHT
void DHT_Rst(void)	   
{                 
	DHT_IO_OUT(); 	//SET OUTPUT
  DHT_DQ_OUT=0; 	//拉低DQ
  delay_ms(20);    	//拉低至少18ms
  DHT_DQ_OUT=1; 	//DQ=1 
	delay_us(30);     	//主机拉高20~40us
}
//等待DHT的回应
//返回1:未检测到DHT的存在
//返回0:存在
u8 DHT_Check(void) 	   
{   
	u8 retry=0;
	DHT_IO_IN();//SET INPUT	 
    while (DHT_DQ_IN&&retry<100)//DHT会拉低40~80us
	{
		retry++;
		delay_us(1);
	};	 
	if(retry>=100)return 1;
	else retry=0;
    while (!DHT_DQ_IN&&retry<100)//DHT拉低后会再次拉高40~80us
	{
		retry++;
		delay_us(1);
	};
	if(retry>=100)return 1;	    
	return 0;
}
//从DHT读取一个位
//返回值：1/0
u8 DHT_Read_Bit(void) 			 
{
 	u8 retry=0;
	while(DHT_DQ_IN&&retry<100)//等待变为低电平
	{
		retry++;
		delay_us(1);
	}
	retry=0;
	while(!DHT_DQ_IN&&retry<100)//等待变高电平
	{
		retry++;
		delay_us(1);
	}
	delay_us(40);//等待40us
	if(DHT_DQ_IN)
		return 1;
	else return 0;		   
}
//从DHT读取一个字节
//返回值：读到的数据
u8 DHT_Read_Byte(void)    
{        
    u8 i,dat;
    dat=0;
	for (i=0;i<8;i++) 
	{
   		dat<<=1; 
	    dat|=DHT_Read_Bit();
    }						    
    return dat;
}
//从DHT读取一次数据
//temp:温度值(范围:0~50°)
//humi:湿度值(范围:20%~90%)
//返回值：0,正常;1,读取失败
u8 DHT_Read_Data(u8 *temp,u8 *humi)    
{        
 	u8 buf[5];
	u8 i;
	DHT_Rst();
	if(DHT_Check()==0)
	{
		for(i=0;i<5;i++)//读取40位数据
		{
			buf[i]=DHT_Read_Byte();
		}
		if((buf[0]+buf[1]+buf[2]+buf[3])==buf[4])
		{
			*humi=buf[0];
			*temp=buf[2];
		}
	}else return 1;
	return 0;	    
}
//初始化DHT的IO口 DQ 同时检测DHT的存在
//返回1:不存在
//返回0:存在    	 
u8 DHT_Init(void)
{
	GPIO_InitTypeDef  GPIO_InitStructure;

  RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOB, ENABLE);//使能GPIOG时钟

  //GPIOF9,F10初始化设置
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9 ;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;//普通输出模式
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;//推挽输出
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;//50MHz
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;//上拉
  GPIO_Init(GPIOB, &GPIO_InitStructure);//初始化
	DHT_Rst();
	return DHT_Check();
}







